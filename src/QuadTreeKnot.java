/**
 * QuadTree Knot class.
 */
public class QuadTreeKnot {
    public int x, y;
    public String label;

    /**
     * Constructor for QuadTreeKnot.
     * 
     * @param x x-coordinate of the QuadTreeKnot.
     * @param y y-coordinate of the QuadTreeKnot.
     * @param label label of the QuadTreeKnot.
     */
    public QuadTreeKnot(int x, int y, String label) {
        this.x = x;
        this.y = y;
        this.label = label;
    }
}
